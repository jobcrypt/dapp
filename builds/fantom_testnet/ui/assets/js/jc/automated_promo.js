			location.append(text(event.location));

			var country 	= ce("li");
			ul.append(country);
			country.append(text(event.country));

			var tyme 		= ce("li");
			ul.append(tyme);
			tyme.append(text(event.startTime +" - "+event.endTime));

			var timezone 	= ce("li");
			ul.append(timezone);
			timezone.append(text(event.timezone));

			var schedule 	= ce("li");
			ul.append(schedule);
			var link = ce("a");
